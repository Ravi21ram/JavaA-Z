package com.jsp.details;

public class Details {
	private String name;
	private  long accno ;
	private double bal;
	public String getname( )
	{
		return name ;
	}
	public void setname(String name )
	{
		this.name=name;
	}
	public double getaccno( )
	{
		return accno ;
	}
	public void setaccno(long accno )
	{
		this.accno=accno;
	}
	public double  getbal( )
	{
		return bal;
	}
	public void setbal( double bal )
	{
		this.bal=bal;
	}

}
